-- ---------------------------------------------------------------------------
-- PII retention: null raw_text after 90 days
-- ---------------------------------------------------------------------------
-- Schedule this as a cron job (pg_cron extension, or external cron via n8n).
-- parsed_json is kept permanently; only the raw email body is cleared.
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION purge_old_raw_text()
RETURNS void LANGUAGE plpgsql AS $$
BEGIN
  UPDATE job_events
  SET raw_text = NULL
  WHERE raw_text IS NOT NULL
    AND created_at < NOW() - INTERVAL '90 days';

  RAISE NOTICE 'purge_old_raw_text: completed at %', NOW();
END;
$$;

COMMENT ON FUNCTION purge_old_raw_text() IS
  'Nulls raw_text in job_events older than 90 days. '
  'Schedule via pg_cron: SELECT cron.schedule(''0 3 * * *'', ''SELECT purge_old_raw_text()'');'
  'or via n8n Schedule Trigger → Postgres Execute Query.';

