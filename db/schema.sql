-- Application tracker view (mirrors Google Sheets / Notion layout)
CREATE OR REPLACE VIEW v_applications AS
SELECT
  j.id                    AS job_id,
  c.name                  AS company,
  e.name                  AS recruiter,
  e.employer_id           AS recruiter_email,
  j.job_title             AS role,
  j.location,
  j.current_stage         AS stage,
  j.priority,
  j.interview_date,
  j.response_deadline,
  j.application_deadline,
  j.call_count,
  j.last_call_date,
  j.salary_min,
  j.salary_max,
  j.salary_currency,
  j.salary_period,
  j.work_mode,
  j.seniority,
  j.tech_stack,
  j.user_rating,
  j.source_url,
  j.summary,
  j.created_at,
  j.updated_at
FROM jobs j
JOIN companies c ON c.company_id = j.company_id
LEFT JOIN employers e ON e.employer_id = j.employer_id
ORDER BY
  j.priority_level ASC NULLS LAST,
  j.interview_date ASC NULLS LAST,
  j.updated_at DESC;

COMMENT ON VIEW v_applications IS 'Main application tracker view. Source for Google Sheets sync.';

-- Recent events feed (last 7 days)
CREATE OR REPLACE VIEW v_recent_events AS
SELECT
  je.id,
  je.email_date,
  c.name                  AS company,
  j.job_title             AS role,
  je.category,
  je.stage,
  je.priority,
  je.interview_date,
  je.call_platform,
  je.input_source,
  je.created_at
FROM job_events je
LEFT JOIN jobs j ON j.id = je.job_id
LEFT JOIN companies c ON c.company_id = j.company_id
WHERE je.created_at >= NOW() - INTERVAL '7 days'
  AND je.is_job_related = TRUE
ORDER BY je.created_at DESC;

COMMENT ON VIEW v_recent_events IS 'Events feed for the last 7 days. Use for Telegram notifications.';

-- ---------------------------------------------------------------------------
-- Database roles (least privilege)
-- ---------------------------------------------------------------------------
-- Run once as superuser after schema creation.
-- Passwords must be set externally (from env, never hardcoded here).
-- ---------------------------------------------------------------------------

DO $$
BEGIN
  -- Application user: read/write job data only
  IF NOT EXISTS (SELECT FROM pg_roles WHERE rolname = 'jobradar_app') THEN
    CREATE ROLE jobradar_app LOGIN;
  END IF;

  -- Read-only user: analytics, Sheets sync, Grafana
  IF NOT EXISTS (SELECT FROM pg_roles WHERE rolname = 'jobradar_readonly') THEN
    CREATE ROLE jobradar_readonly LOGIN;
  END IF;
END
$$;

GRANT SELECT, INSERT, UPDATE ON companies, employers, jobs, job_events TO jobradar_app;
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA public TO jobradar_app;
-- job_events: app may INSERT but NOT UPDATE or DELETE (append-only)
REVOKE UPDATE, DELETE ON job_events FROM jobradar_app;

GRANT SELECT ON ALL TABLES IN SCHEMA public TO jobradar_readonly;

